//Fisher-Yates Shuffle
function shuffle(a, b, c) {
  for (let m = a.length - 1; m > 0; m--) {
    const j = Math.floor(Math.random() * (m + 1));
    [a[m], a[j]] = [a[j], a[m]];
    [b[m], b[j]] = [b[j], b[m]];
    [c[m], c[j]] = [c[j], c[m]];
  }
  i = 0;
  simulateEnter();
  return a, b, c;
}

